#include "libft.h"

void	ft_putnbr(int n)
{
	int tmp;
	int sign;

	sign = (n < 0 ? -n : n);
	if (n == -2147483648)
		write(1, "-2147483648", 11);
	else if (n < 0)
	{
		write(1, "-", 1);
		ft_putnbr(sign);
	}
	else if (n > 9)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
	{
		tmp = n + '0';
		write(1, &tmp, 1);
	}
}
